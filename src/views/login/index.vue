<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <!-- 放置标题图片 @是设置的别名-->
        <h3 class="title"><img src="@/assets/common/login-logo.png" alt=""></h3>
      </div>

      <el-form-item prop="mobile">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="mobile"
          v-model="loginForm.mobile"
          placeholder="请输入手机号"
          name="mobile"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="请输入密码"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-button class="loginBtn" :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">登录</el-button>

      <div class="tips">
        <span style="margin-right:20px;">username: admin</span>
        <span> password: any</span>
      </div>

    </el-form>
  </div>
</template>

<script>
// 导入校验手机号函数
import { validMobile } from '@/utils/validate'
// 映射 actions里面的函数
import { mapActions } from 'vuex'

export default {
  name: 'Login',
  data() {
    // 自定义校验规则
    const validateMobile = (relue, value, callback) => {
      if (validMobile(value)) {
        callback()
      } else {
        callback(new Error('手机号格式不规范'))
      }
    }
    return {
      // 表单数据对象
      loginForm: {
        mobile: '13800000002',
        password: '123456'
      },
      // 表单校验对象
      loginRules: {
        mobile: [
          { required: true, message: '请输入手机号', trigger: ['blur', 'change'] },
          { validator: validateMobile, trigger: ['blur', 'change'] }
        ],
        password: [
          { required: true, message: '请输入密码', trigger: ['blur', 'change'] },
          { min: 6, max: 15, message: '密码长度为6~15位', required: true, trigger: ['blur', 'change'] }]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    ...mapActions('user', ['getToken']),
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    // 登录函数
    handleLogin() {
      // 表单兜底校验
      this.$refs.loginForm.validate(async valid => {
        if (!valid) return
        // 发送ajax
        // const res = await this.$http.post('http://localhost:8848/api/sys/login', this.loginForm)
        // const res = await reqLogin(this.loginForm)
        // 得到数据 将token保存到vuex中
        // 调用getToken函数 并将数据传参
        this.getToken(this.loginForm)
      })
    }
  }
}
</script>

